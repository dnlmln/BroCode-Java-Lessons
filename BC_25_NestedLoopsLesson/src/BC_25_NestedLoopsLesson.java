public class BC_25_NestedLoopsLesson {

    public static void main(String[] args){

        // nested loop = A loop inside another loop
        //                  Used often within matrices or Data Structure & Algorithm

        for(int i = 1; i <= 4; i++){
            for(int j = 1; j <= 3; j++){
                System.out.print(j + " ");
            }
            System.out.println(" ");
        }
    }
}
